               <h2 id = "zag2"> 2. Программирование</h2>

               <p>В программировании я вижу творческий процесс, инструмент, с помощью которого я могу реализовать какие-то свои задумки.</p>
               <p><b>Веб-программирование. </b>Простыми словами, веб программирование — это создание сайтов и программ, функционирующих в сети Интернет. Все сайты, с которыми Вы ежедневно имеете дело, созданы именно благодаря технологиям веб-программирования. Однако главное отличие между ними заключается в наборе функций и возможностей, которые предлагает для целевой аудитории тот или иной сайт. Сегодня бизнес страницы с просто красивым текстом и яркой картинкой не привлекут внимание клиента так, как сделают это сайты, в основу которых положены интерактивные компоненты. То есть такие компоненты, которые помогают «установить связь» с потенциальным пользователем и поддерживать с ним надлежащее взаимодействие. Сюда и приходят на помощь веб-разработчики, задачей которых является спроектировать живой и многофункциональный сайт и оснастить его эффективными интерактивными инструментами обратной связи, подписки, рассылки и многим другим.</p>

               <p><b>Объе́ктно-ориенти́рованное программи́рование (ООП). </b>Это — методология программирования, основанная на представлении программы в виде совокупности объектов, каждый из которых является экземпляром определённого класса, а классы образуют иерархию наследования. </p>

               <p><b>Аналитическое программирование. </b>Это было выбрано потому, что АП представлял синтез аналитического решения, основываясь на эволюционном алгоритме. АП был основан на специальном наборе математических объектов и операций. Набор операций представлял собой функции, операторы и терминалы (также как в ГП), которые обычно являлись константами или независимыми переменными.</p>

               <p><b>Разработка игр. </b>Современная разработка мобильных мини-игр – это больше работа художника, чем программиста. Для программной реализации достаточно самых базовых представления о разработке. Конечно, если делается большой и качественный продукт с коммерческой целью – все совсем по-другому. ... Делать игру – это интересно. Иногда даже более интересно, чем играть в нее. В отличии от работы в команде над крупным проектом это дает маленькую радость личной победы. </p>

