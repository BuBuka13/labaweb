              <p><b>Биолоия. </b>Биология – это обширная наука о жизни, включающая множество отдельных, но взаимосвязанных наук. Биология как наука включает все знания о природе, структуре, поведении и функции всех живых существ на планете Земля. Каждому человеку необходимо какое-то увлечение в жизни, способное доставлять удовольствие и забивать голову приятными мелочами, никак не связанными с бытовыми проблемами..</p>

