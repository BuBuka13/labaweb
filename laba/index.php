              <p>Обучаясь в вузе, многие студенты самостоятельно изучают определенные науки, для них это как увлечение, им нравится познавать все больше и больше вне зависимости от выбранной специальности. Например, знакомый учится на программиста, но ему параллельно нравится изучать и читать про философию.</p>
              <p><b>Химия. </b>Любительская химия обычно делается с любыми химическими веществами, имеющимися в распоряжении в уединении дома.</p>

